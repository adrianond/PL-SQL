CREATE OR REPLACE PROCEDURE PRC_CONSULTA_COLABORADOR(EMPLOYEE_ID NUMBER) IS

V_NOME             EMPLOYEES.FIRST_NAME%TYPE;
V_SOBRENOME        EMPLOYEES.LAST_NAME%TYPE; 

BEGIN
  SELECT FIRST_NAME, LAST_NAME
  INTO V_NM_COLABORADOR, V_SOBRENOME
  FROM EMPLOYEES WHERE EMPLOYEE_ID = EMPLOYEE_ID;
  DBMS_OUT.PUT_LINE('NOME: || V_NOME || SOBRENOME || V_SOBRENOME');
END;

END PRC_CONSULTA_COLABORADOR;